#define __CO_timer_h__

#include "applicfg.h"
#include "Timer.h"



#define TIMER_HANDLE INTEGER8
#define TIMEVAL UNS32
#define TIMER_NONE -1
#define MS_TO_TIMEVAL(ms) (ms)


